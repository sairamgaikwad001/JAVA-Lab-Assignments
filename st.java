class st
{
   public static void main(String args[])
  {
  
   int ch = 2;
   
   
   switch(ch)
   { 
     case 1:
        System.out.println("Ram is Younger.");
        break;
        
     case 2:
        System.out.println("Shyam is Younger.");
        break;
        
     case 3:
        System.out.println("Roshan is Younger.");
        break;
        
     case 4:
       default:
          System.out.println("Wrong Choice.");
    }
  }
  
}
    
    
