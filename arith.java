/* Write Java program in which perform the arithmetic operations on two no's. */


class arith
{
  public static void main(String args[])
  {
    int a = 10;
    int b = 50;
    
    System.out.println("The Arithmetic operations on following no's:\n\n a = " + a + "\n b = " + b);
    System.out.println("\n Addition: " + a + " + " + b + " = " + (a+b));
    System.out.println(" Subtraction: " + a + " - " + b + " = " + (a-b));
    System.out.println(" Multiplication: " + a + " * " + b + " = " + (a*b));
    System.out.println(" Division: " + a + " / " + b + " = " + (a/b));
    System.out.println(" Remainder: " + a + " % " + b + " = " + (a%b));
  }
}
    
    
